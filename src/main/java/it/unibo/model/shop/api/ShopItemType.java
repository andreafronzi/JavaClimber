package it.unibo.model.shop.api;


/**
 * Enumeration of the types of items available in the shop.
 */
public enum ShopItemType {

    /**
     * Skin type item.
     */
    SKIN,
    /**
     * Permanent upgrade type item.
     */
    PERMANENT_UPGRADE,
    /**
     * Temporary upgrade for N matches type item.
     */
    TEMPORARY_UPGRADE
}
